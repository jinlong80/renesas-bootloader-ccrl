bool_t bsp_di_is_hi(bsp_di_dev_id_t id)
{
    ASSERT(id == BSP_DI_DEV_ID_PIR);
    
    if (id == BSP_DI_DEV_ID_BUTTON)
    {
        if (P5 & 0x01U)
        {
            return TRUE;
        }
        else
        {
            return FALSE;
        }
    }
    return FALSE;
}
 
